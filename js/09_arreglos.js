// Arrays o Arreglos

const tecnologias = [20, 30, true, 'React', 'JavaScript']

console.log(typeof tecnologias) // object

// obtener el valor de un elemento del arreglo por su posición
console.log(tecnologias[3])

// obtener el tamaño de elementos del arreglo
console.log(tecnologias.length)

// imprimir el arreglo como si fuera un string
console.log(tecnologias.toString())


